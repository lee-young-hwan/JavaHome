
public class SwitchEx {
	public static void main(String[] args) {
		
		int score = 95;
		
		// int 이하의 정수값, String값
		switch(score) {
			case 95 : System.out.println("95점입니다"); 
		
			
			case 94 : System.out.println("94점입니다"); 
			
			
			case 93 : System.out.println("93점입니다"); 
			
			
			default : System.out.println("잘못된 값입니다.");
		}
	}
}
